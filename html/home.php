    <h2>Time zone</h2>
    
    <p>The local area operates on GMT+1.  Both sides of the border observe daylight saving time.  TimeAndDate provide information about the <a href="http://www.timeanddate.com/worldclock/timezone.html?n=195">French timezone</a> and <a href="http://www.timeanddate.com/worldclock/timezone.html?n=87">Swiss timezone</a> (fortunately these are identical!).
    
    <h2>Climate</h2>
    
    <p>The local area is temperate, with temperatures warmer than London in the Summer, but cooler in the Winter.  The mountains are colder than the valley.  There are often light winds near Lake Geneva.  WorldTravels provides information about <a href="http://www.wordtravels.com/Cities/Switzerland/Geneva/Climate">the climate of Geneva</a>.</p>
    
    <h2>Border control</h2>
    
    <p>If you are an EU, US, Canadian, Australian or New Zealand citizen you should have no problems getting through passport control at the airport.  Depending on your nationanality you may be able to travel freely to Swizterland and France.  See the [pdf]<a href="http://www.bfm.admin.ch/content/dam/data/migration/rechtsgrundlagen/weisungen_und_kreisschreiben/weisungen_visa/anh1-liste1_vorschriften-nach-staat-e.pdf">Swiss immigration notices</a> for more information.  Occassionally people are stopped by the Swiss authorities and may be asked to produce ID.  Please bring your passport with you and be prepared to show it if asked!</p>
    
    