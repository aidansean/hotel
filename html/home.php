    <h2>Language</h2>
    
    <p>The local language is French.  The official languages of Switzerland are French, German, Italian and Romansch, although in the Geneva area the predominant language is French.  At the airport you can expect the staff to speak fluent English, French, German and Italian.  Some staff may also speak other European languages.  At CERN most staff speak English and French, and nearly all physicists speak some English.  In Geneva you can expect most restaurant owners and shopkeepers to speak enough English to separate you from your money.  Aidan speaks a little French, and even less German.</p>
    
