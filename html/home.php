    <h2>Currency</h2>
    
    <p>There are two currencies in use in the local area.  On the French side of the border (Saint Genis, Ferney Voltaire, Gex, the Jura) the local cuurency is the Euro (&#8364;).  On the Swiss side of the border (CERN, Geneva, the Alps) the currency is the Swiss Franc (CHF).  You can use both currencies at the Val Thoiry shopping centre, the airport, and at CERN.  You can find exchange rates at <a href="http://www.xe.com/ucc/">xe</a>.
    
    <h2>Public transport and arrival</h2>
    
    <p>Transport in the Geneva area is largely managed by <a href="http://www.tpg.ch/fr/index.php">Transport publics genevois</a> (TPG).  There are trams which radiate from the centre of Geneva, and buses in many other areas.  The main hub for all the lines is the Cornavin station in central Geneva.</p>
    
    <p>Tickets are purchased using machines at bus and tram stops, or on the bus.  A ticket is good for one hour and can be used on multiple buses or trams.  A ticket for the French side of the border costs &#8364;1.20 or CHF 1.80.  A ticket for the Swiss side of the border (which can also be used in France) costs CHF 3.00.</p>
    
    <p>To get to the hotel from the airport, or CERN, you can take the Y bus.  Get off at the Champ-Fusy stop.  <em>You can get a free ticket at the airport</em>.  To do this, use one of the machines in the baggage claim area, to the right of the main exits.  (To the right of the green "Nothing to declare" doors.)</p>
